BRAND_ANALYSIS_CACHE: Dict[str, dict] = {}
BRAND_ANALYSIS_CACHE_FILE = OUTPUT_DIR / "brand_analysis_cache.json"


def load_brand_cache():
    if BRAND_ANALYSIS_CACHE_FILE.exists():
        data = json.loads(BRAND_ANALYSIS_CACHE_FILE.read_text(encoding="utf-8"))
        BRAND_ANALYSIS_CACHE.update(data)
        logger.info(f"Loaded {len(BRAND_ANALYSIS_CACHE)} cached brand analyses")


def save_brand_cache():
    BRAND_ANALYSIS_CACHE_FILE.write_text(
        json.dumps(BRAND_ANALYSIS_CACHE, indent=2), encoding="utf-8"
    )


def get_brand_analysis(client: OpenAI, product: dict) -> dict:
    key = product["prompt"]
    if key in BRAND_ANALYSIS_CACHE:
        return BRAND_ANALYSIS_CACHE[key]

    prompt = f"""You are a brand strategist. For the product '{product["prompt"]}' by {product["brand"]},
provide a compact brand analysis in JSON with exactly these fields:
- tone: 3 comma-separated tone descriptors (e.g. "sophisticated, timeless, authoritative")
- visual_direction: one sentence describing the visual style appropriate for this brand
- ad_style: one sentence naming the advertising technique (e.g. "lifestyle aspiration storytelling")

Respond ONLY with valid JSON, no markdown."""

    resp = client.chat.completions.create(
        model="gpt-4.1-mini-2025-04-14",
        messages=[{"role": "user", "content": prompt}],
        response_format={"type": "json_object"},
        temperature=0,
    )
    analysis = json.loads(resp.choices[0].message.content)
    BRAND_ANALYSIS_CACHE[key] = analysis
    save_brand_cache()
    return analysis


