PAIRWISE_PROMPT = """You are an advertising creative director evaluating two creative briefs for the same product.

INPUT PROMPT: {product_prompt}

BRIEF 1:
{brief1_json}

BRIEF 2:
{brief2_json}

For each dimension, select which brief is BETTER and explain why in one sentence:
1. HEADLINE: Which headline is more brand-specific, attention-grabbing, and concise?
2. VISUAL CONCEPT: Which caption describes a more original, concrete, producible visual scene?
3. STRATEGIC THINKING: Which brief demonstrates a more coherent advertising technique with stronger product fit?
4. BRAND ALIGNMENT: Which brief better matches the brand's actual voice, colors, and market position?
5. OVERALL: If you could only produce ONE ad, which brief would you choose?

For each dimension respond with EXACTLY one of:
- "BRIEF_1" - Brief 1 is clearly better
- "BRIEF_2" - Brief 2 is clearly better
- "TIE" - No meaningful difference

Respond in JSON:
{{
  "headline": {{"winner": "BRIEF_1|BRIEF_2|TIE", "reason": "..."}},
  "visual_concept": {{"winner": "BRIEF_1|BRIEF_2|TIE", "reason": "..."}},
  "strategic_thinking": {{"winner": "BRIEF_1|BRIEF_2|TIE", "reason": "..."}},
  "brand_alignment": {{"winner": "BRIEF_1|BRIEF_2|TIE", "reason": "..."}},
  "overall": {{"winner": "BRIEF_1|BRIEF_2|TIE", "reason": "..."}}
}}"""


def run_pairwise(client: OpenAI, product: dict, brief_a: dict, brief_b: dict,
                 model_a: str, model_b: str, position: int) -> Optional[dict]:
    """position=1: a is Brief 1, b is Brief 2. position=2: swapped."""
    if position == 1:
        b1, b2 = brief_a, brief_b
        b1_model, b2_model = model_a, model_b
    else:
        b1, b2 = brief_b, brief_a
        b1_model, b2_model = model_b, model_a

    prompt = PAIRWISE_PROMPT.format(
        product_prompt=product["prompt"],
        brief1_json=json.dumps(b1, indent=2),
        brief2_json=json.dumps(b2, indent=2),
    )
    try:
        resp = client.chat.completions.create(
            model=JUDGE_MODEL,
            messages=[{"role": "user", "content": prompt}],
            response_format={"type": "json_object"},
            temperature=JUDGE_TEMPERATURE,
        )
        result = json.loads(resp.choices[0].message.content)
        result["position"] = position
        result["brief1_model"] = b1_model
        result["brief2_model"] = b2_model
        result["model_a"] = model_a
        result["model_b"] = model_b
        result["product"] = product["prompt"]
        return result
    except Exception as e:
        logger.error(f"Pairwise comparison failed: {e}")
        return None


def resolve_pairwise(pos1: dict, pos2: dict, model_a: str, model_b: str) -> dict:
    """
    Resolve two position-swapped pairwise results.
    Returns: winner = model_a | model_b | TIE | UNSTABLE
    """
    def get_winner_model(result: dict, dimension: str) -> Optional[str]:
        d = result.get(dimension, {})
        raw = d.get("winner", "TIE")
        if raw == "TIE":
            return "TIE"
        # Map BRIEF_1/BRIEF_2 back to model
        b1m = result["brief1_model"]
        b2m = result["brief2_model"]
        if raw == "BRIEF_1":
            return b1m
        elif raw == "BRIEF_2":
            return b2m
        return "TIE"

    resolved = {}
    dimensions = ["headline", "visual_concept", "strategic_thinking", "brand_alignment", "overall"]
    for dim in dimensions:
        w1 = get_winner_model(pos1, dim)
        w2 = get_winner_model(pos2, dim)
        if w1 == w2:
            resolved[dim] = {"winner": w1, "stable": True}
        elif w1 == "TIE" or w2 == "TIE":
            # One said tie, one said a winner — take the non-tie as directional but note
            winner = w2 if w1 == "TIE" else w1
            resolved[dim] = {"winner": winner, "stable": True, "note": "one_position_tie"}
        else:
            # Position flip changed the winner — unstable
            resolved[dim] = {"winner": "UNSTABLE", "stable": False}
    return resolved


def phase3_pairwise(client: OpenAI, skip: bool = False):
    PAIRWISE_DIR.mkdir(parents=True, exist_ok=True)

    # Load all briefs indexed by (slug, model)
    briefs: Dict[str, Dict[str, dict]] = {}
    for bf in BRIEFS_DIR.glob("*.json"):
        parts = bf.stem.rsplit("_", 1)
        if len(parts) != 2:
            continue
        slug, model = parts[0], parts[1]
        if slug not in briefs:
            briefs[slug] = {}
        record = json.loads(bf.read_text(encoding="utf-8"))
        briefs[slug][model] = record

    model_pairs = [("baseline", "sft"), ("sft", "dpo"), ("baseline", "dpo")]
    total = 0

    for product in EVAL_PRODUCTS:
        slug = product_slug(product)
        slug_briefs = briefs.get(slug, {})

        for model_a, model_b in model_pairs:
            pair_name = f"{slug}_{model_a}_vs_{model_b}"
            out_path = PAIRWISE_DIR / f"{pair_name}_resolved.json"

            if skip and out_path.exists():
                total += 1
                continue

            if model_a not in slug_briefs or model_b not in slug_briefs:
                logger.warning(f"  Missing briefs for {pair_name}")
                continue

            brief_a = slug_briefs[model_a]["brief"]
            brief_b = slug_briefs[model_b]["brief"]

            logger.info(f"  Pairwise: {pair_name}")

            # Position 1
            p1_path = PAIRWISE_DIR / f"{pair_name}_pos1.json"
            if skip and p1_path.exists():
                pos1 = json.loads(p1_path.read_text(encoding="utf-8"))
            else:
                pos1 = run_pairwise(client, product, brief_a, brief_b, model_a, model_b, position=1)
                if pos1:
                    p1_path.write_text(json.dumps(pos1, indent=2), encoding="utf-8")
                time.sleep(0.4)

            # Position 2 (swapped)
            p2_path = PAIRWISE_DIR / f"{pair_name}_pos2.json"
            if skip and p2_path.exists():
                pos2 = json.loads(p2_path.read_text(encoding="utf-8"))
            else:
                pos2 = run_pairwise(client, product, brief_a, brief_b, model_a, model_b, position=2)
                if pos2:
                    p2_path.write_text(json.dumps(pos2, indent=2), encoding="utf-8")
                time.sleep(0.4)

            if pos1 and pos2:
                resolved = resolve_pairwise(pos1, pos2, model_a, model_b)
                out = {
                    "product": product["prompt"],
                    "slug": slug,
                    "model_a": model_a,
                    "model_b": model_b,
                    "resolved": resolved,
                }
                out_path.write_text(json.dumps(out, indent=2), encoding="utf-8")
                total += 1

                overall = resolved.get("overall", {}).get("winner", "?")
                logger.info(f"    overall winner: {overall}")

    logger.info(f"Pairwise complete: {total} resolved comparisons")
    return total


