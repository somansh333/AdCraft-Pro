def compute_aggregate_stats():
    scored_files = [p for p in POINTWISE_DIR.glob("*_scored.json") if "run2" not in p.name]

    # Group by model
    model_scores: Dict[str, List[float]] = {"baseline": [], "sft": [], "dpo": []}
    model_dims: Dict[str, Dict[str, List[float]]] = {
        m: {d: [] for d in DIMENSION_GROUPS} for m in model_scores
    }
    product_results: Dict[str, Dict[str, dict]] = {}

    for sf in scored_files:
        record = json.loads(sf.read_text(encoding="utf-8"))
        model = record.get("model_name", "")
        if model not in model_scores:
            continue
        score = record["normalized_score"]
        model_scores[model].append(score)
        for dim, subs in DIMENSION_GROUPS.items():
            ds = record.get("dimension_scores", {})
            model_dims[model][dim].append(ds.get(dim, 0))

        product_str = record.get("product", {})
        if isinstance(product_str, dict):
            product_str = product_str.get("prompt", "?")
        if product_str not in product_results:
            product_results[product_str] = {}
        product_results[product_str][model] = {
            "score": score,
            "headline": record.get("product", {}).get("prompt", "?") if isinstance(record.get("product"), dict) else "?",
        }
        # Get headline from brief file
        brief_file = record.get("brief_file", "")
        if brief_file and Path(brief_file).exists():
            br = json.loads(Path(brief_file).read_text(encoding="utf-8"))
            product_results[product_str][model]["headline"] = br["brief"].get("headline", "?")

    agg = {}
    for model, scores in model_scores.items():
        if not scores:
            continue
        arr = np.array(scores)
        agg[model] = {
            "n": len(scores),
            "mean": round(float(arr.mean()), 2),
            "std": round(float(arr.std(ddof=1) if len(arr) > 1 else 0), 2),
            "median": round(float(np.median(arr)), 2),
            "min": round(float(arr.min()), 2),
            "max": round(float(arr.max()), 2),
            "scores": scores,
        }
        for dim in DIMENSION_GROUPS:
            dim_arr = np.array(model_dims[model][dim])
            agg[model][f"dim_{dim}_mean"] = round(float(dim_arr.mean()) if len(dim_arr) else 0, 2)

    # Statistical tests
    tests = {}
    pairs = [("baseline", "sft"), ("sft", "dpo"), ("baseline", "dpo")]
    for ma, mb in pairs:
        if ma in agg and mb in agg:
            sa, sb = np.array(agg[ma]["scores"]), np.array(agg[mb]["scores"])
            t, p = scipy_stats.ttest_ind(sa, sb, equal_var=False)
            delta = float(sb.mean() - sa.mean())
            pooled_std = float(np.sqrt((sa.std(ddof=1)**2 + sb.std(ddof=1)**2) / 2))
            cohens_d = delta / pooled_std if pooled_std > 0 else 0
            se = float(np.sqrt(sa.std(ddof=1)**2/len(sa) + sb.std(ddof=1)**2/len(sb)))
            tests[f"{ma}_vs_{mb}"] = {
                "delta": round(delta, 2),
                "t_stat": round(float(t), 3),
                "p_value": round(float(p), 4),
                "cohens_d": round(cohens_d, 3),
                "ci_lower": round(delta - 1.96*se, 2),
                "ci_upper": round(delta + 1.96*se, 2),
                "significant": bool(p < 0.05),
            }

    return agg, tests, product_results


def compute_pairwise_stats():
    resolved_files = sorted(PAIRWISE_DIR.glob("*_resolved.json"))
    # model pair -> dimension -> counts
    pair_results: Dict[str, Dict[str, Dict[str, int]]] = {}
    dimensions = ["headline", "visual_concept", "strategic_thinking", "brand_alignment", "overall"]

    for rf in resolved_files:
        record = json.loads(rf.read_text(encoding="utf-8"))
        ma = record["model_a"]
        mb = record["model_b"]
        pair_key = f"{ma}_vs_{mb}"
        if pair_key not in pair_results:
            pair_results[pair_key] = {d: {"a_wins": 0, "b_wins": 0, "ties": 0, "unstable": 0} for d in dimensions}

        resolved = record["resolved"]
        for dim in dimensions:
            r = resolved.get(dim, {})
            winner = r.get("winner", "TIE")
            stable = r.get("stable", True)
            if not stable:
                pair_results[pair_key][dim]["unstable"] += 1
            elif winner == ma:
                pair_results[pair_key][dim]["a_wins"] += 1
            elif winner == mb:
                pair_results[pair_key][dim]["b_wins"] += 1
            else:
                pair_results[pair_key][dim]["ties"] += 1

    # Compute win rates
    stats = {}
    for pair_key, dims in pair_results.items():
        stats[pair_key] = {}
        for dim, counts in dims.items():
            total = counts["a_wins"] + counts["b_wins"] + counts["ties"] + counts["unstable"]
            valid = counts["a_wins"] + counts["b_wins"] + counts["ties"]
            b_win_rate = (counts["b_wins"] + 0.5 * counts["ties"]) / valid if valid > 0 else 0
            # Binomial test: is b_win_rate > 0.5?
            if valid >= 2:
                binom = scipy_stats.binomtest(counts["b_wins"], n=valid, p=0.5, alternative="greater")
                binom_p = float(binom.pvalue)
            else:
                binom_p = 1.0
            stats[pair_key][dim] = {
                **counts,
                "total": total,
                "valid": valid,
                "b_win_rate": round(b_win_rate, 3),
                "binom_p": round(binom_p, 4),
                "b_significant": binom_p < 0.05,
            }

    return stats


