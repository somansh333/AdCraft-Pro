def build_user_message(product: dict, analysis: dict) -> str:
    return (
        f"Create an ad for:\n"
        f"Brand: {product['brand']}\n"
        f"Product type: {product['product']}\n"
        f"Tone: {analysis.get('tone', 'professional')}\n"
        f"Visual style: {analysis.get('visual_direction', 'modern')}\n"
        f"Conceptual technique: {analysis.get('ad_style', 'product showcase')}\n"
        f"Format: image"
    )


def generate_brief(client: OpenAI, model_id: str, user_message: str, temperature: float = 0.75) -> Optional[dict]:
    try:
        resp = client.chat.completions.create(
            model=model_id,
            messages=[
                {"role": "system", "content": CREATIVE_BRIEF_SYSTEM_PROMPT},
                {"role": "user", "content": user_message},
            ],
            response_format={"type": "json_object"},
            temperature=temperature,
        )
        return json.loads(resp.choices[0].message.content)
    except Exception as e:
        logger.error(f"Brief generation failed ({model_id}): {e}")
        return None


def phase1_generate_briefs(client: OpenAI, sft_model: str, dpo_model: str, skip: bool = False):
    BRIEFS_DIR.mkdir(parents=True, exist_ok=True)
    load_brand_cache()

    model_map = {
        "baseline": BASELINE_MODEL,
        "sft": sft_model,
        "dpo": dpo_model,
    }

    total = 0
    for i, product in enumerate(EVAL_PRODUCTS):
        slug = product_slug(product)
        logger.info(f"[{i+1}/{len(EVAL_PRODUCTS)}] {product['prompt']}")

        # Brand analysis (cached)
        analysis = get_brand_analysis(client, product)
        user_msg = build_user_message(product, analysis)

        for model_name, model_id in model_map.items():
            out_path = BRIEFS_DIR / f"{slug}_{model_name}.json"
            if skip and out_path.exists():
                logger.info(f"  {model_name}: skipping (file exists)")
                total += 1
                continue

            brief = generate_brief(client, model_id, user_msg)
            if brief is None:
                logger.warning(f"  {model_name}: FAILED to generate brief")
                continue

            record = {
                "product": product,
                "model_name": model_name,
                "model_id": model_id,
                "user_message": user_msg,
                "brief": brief,
                "generated_at": datetime.now().isoformat(),
            }
            out_path.write_text(json.dumps(record, indent=2), encoding="utf-8")
            total += 1
            logger.info(f"  {model_name}: '{brief.get('headline', '?')[:50]}'")
            time.sleep(0.3)

    logger.info(f"Brief generation complete: {total} briefs saved")
    return total


