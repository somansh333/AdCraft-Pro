    print(f"[config] SFT:      {sft_model}")
    print(f"[config] DPO:      {dpo_model}")
    print(f"[config] Products: {len(EVAL_PRODUCTS)}")
    print()

    # Phase 1: Generate briefs
    print("[phase 1] Generating briefs...")
    n_briefs = phase1_generate_briefs(client, sft_model, dpo_model, skip=args.skip_generation)
    print(f"[phase 1] Done: {n_briefs} briefs\n")

    # Phase 2: Pointwise scoring
    print("[phase 2] Pointwise scoring...")
    n_scored = phase2_pointwise_scoring(client, skip=args.skip_scoring)
    print(f"[phase 2] Done: {n_scored} briefs scored\n")

    # Phase 3: Pairwise comparisons
    print("[phase 3] Pairwise comparisons...")
    n_pairwise = phase3_pairwise(client, skip=args.skip_pairwise)
    print(f"[phase 3] Done: {n_pairwise} resolved comparisons\n")

    # Phase 4: Reliability check
    print("[phase 4] Reliability check...")
    if args.skip_reliability:
        reliability = {}
    else:
        reliability = phase4_reliability(client)
    print(f"[phase 4] Done: r={reliability.get('pearson_r', '?')} MAD={reliability.get('mad', '?')}\n")

    # Aggregate stats
    print("[stats] Computing aggregate statistics...")
    agg, tests, product_results = compute_aggregate_stats()
    pairwise_stats = compute_pairwise_stats()

    # Save summary JSON
    summary = {
        "generated_at": datetime.now().isoformat(),
        "n_products": len(EVAL_PRODUCTS),
        "models": {"baseline": BASELINE_MODEL, "sft": sft_model, "dpo": dpo_model},
        "pointwise": agg,
        "statistical_tests": tests,
        "pairwise_stats": pairwise_stats,
        "reliability": reliability,
    }
    summary_path = OUTPUT_DIR / "cbqa_summary.json"
    summary_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")
    print(f"[stats] Summary saved to {summary_path}")

    # Generate report
    report = generate_report(agg, tests, product_results, pairwise_stats, reliability)
    report_path = OUTPUT_DIR / "CBQA_evaluation_report.md"
    report_path.write_text(report, encoding="utf-8")
    print(f"[stats] Report saved to {report_path}")

    # Final summary print
    baseline_m = agg.get("baseline", {})
    sft_m = agg.get("sft", {})
    dpo_m = agg.get("dpo", {})
    t_bd = tests.get("baseline_vs_dpo", {})

    overall_pw = pairwise_stats.get("baseline_vs_dpo", {}).get("overall", {})
    b_wr = overall_pw.get("b_win_rate", 0)
    bp = overall_pw.get("binom_p", 1.0)

    print()
    print("=" * 55)
    print("CBQA EVALUATION -- COMPLETE")
    print("=" * 55)
    print(f"Products evaluated:     {len(EVAL_PRODUCTS)}")
    print(f"Models compared:        3 (baseline, SFT v3, SFT+DPO v3)")
    print(f"Pointwise scores:       {n_scored}")
    print(f"Pairwise comparisons:   {n_pairwise * 2} positions -> {n_pairwise} resolved")
    print()
    print("POINTWISE RESULTS (CBQA 0-100):")
    if baseline_m:
        print(f"  Baseline:     {baseline_m.get('mean', 0):.1f} +/- {baseline_m.get('std', 0):.1f}")
    if sft_m:
        print(f"  SFT v3:       {sft_m.get('mean', 0):.1f} +/- {sft_m.get('std', 0):.1f}")
    if dpo_m:
        print(f"  SFT+DPO v3:   {dpo_m.get('mean', 0):.1f} +/- {dpo_m.get('std', 0):.1f}")
    if t_bd:
        print(f"  Delta (B vs DPO): {t_bd.get('delta', 0):+.1f} (p={t_bd.get('p_value', 1.0):.3f}, Cohen's d={t_bd.get('cohens_d', 0):.3f})")
    print()
    print(f"PAIRWISE WIN RATE (SFT+DPO vs Baseline): {b_wr:.1%} (p={bp:.4f} binomial)")
    if reliability:
        print()
        print("RELIABILITY:")
        print(f"  Pearson r:    {reliability.get('pearson_r', '?')}")
        print(f"  MAD:          {reliability.get('mad', '?')} points")
    print()
    print(f"Report: {report_path}")
    print(f"Summary: {summary_path}")


