def phase4_reliability(client: OpenAI, n: int = RELIABILITY_N):
    """Re-score n random briefs and compute Pearson r and MAD."""
    all_scored = sorted(POINTWISE_DIR.glob("*_run1_scored.json"))
    # fallback: any scored file from run1
    if not all_scored:
        all_scored = sorted(POINTWISE_DIR.glob("*_scored.json"))
        all_scored = [p for p in all_scored if "run2" not in p.name]

    if not all_scored:
        logger.warning("No scored files found for reliability check")
        return {}

    sample = random.sample(all_scored, min(n, len(all_scored)))
    run1_scores = []
    run2_scores = []

    for scored_path in sample:
        record = json.loads(scored_path.read_text(encoding="utf-8"))
        product = record["product"]
        brief_file = record.get("brief_file", "")
        if brief_file and Path(brief_file).exists():
            brief_record = json.loads(Path(brief_file).read_text(encoding="utf-8"))
            brief = brief_record["brief"]
        else:
            # Try to recover brief from scored record
            brief = {k: record.get(k, "") for k in ["headline", "caption", "tone",
                     "visual_style", "conceptual_technique", "call_to_action",
                     "emotion", "typography_style", "color_scheme"]}

        run2_path = POINTWISE_DIR / scored_path.name.replace("_scored.json", "_run2_scored.json")
        if run2_path.exists():
            run2 = json.loads(run2_path.read_text(encoding="utf-8"))
        else:
            logger.info(f"  Reliability re-scoring: {scored_path.stem}")
            run2 = score_brief_pointwise(client, product, brief, run_label="run2")
            if run2 is None:
                continue
            run2["product"] = product
            run2["model_name"] = record.get("model_name", "unknown")
            run2_path.write_text(json.dumps(run2, indent=2), encoding="utf-8")
            time.sleep(0.5)

        run1_scores.append(record["normalized_score"])
        run2_scores.append(run2["normalized_score"])

    if len(run1_scores) < 3:
        return {"note": "insufficient reliability samples"}

    r, p_val = scipy_stats.pearsonr(run1_scores, run2_scores)
    mad = float(np.mean(np.abs(np.array(run1_scores) - np.array(run2_scores))))

    result = {
        "n_pairs": len(run1_scores),
        "pearson_r": round(float(r), 4),
        "p_value": round(float(p_val), 4),
        "mad": round(mad, 2),
        "run1_mean": round(float(np.mean(run1_scores)), 2),
        "run2_mean": round(float(np.mean(run2_scores)), 2),
    }
    logger.info(f"Reliability: r={r:.3f} MAD={mad:.1f} n={len(run1_scores)}")
    return result


